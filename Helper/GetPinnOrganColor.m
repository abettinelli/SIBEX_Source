function OrganColor=GetPinnOrganColor(cellColor)

%PinColor, Window Color
ColorList={'red',  [255,0,0]/255; ...
        'green',  [0,255,0]/255;  ...
        'blue',  [0,0,  255]/255;  ...
        'yellow',  [255,255,0]/255;  ...
        'purple',  [255,0,255]/255;  ...
        'skyblue',  [0,255,255]/255;  ...
        'lavender',  [200,180,255]/255;  ...
        'orange',  [255,150,0]/255;  ...
        'forest',  [34,139,34]/255;  ...
        'slateblue',  [128,0,255]/255;  ...
        'lightblue',  [0,128,255]/255;  ...
        'yellowgreen',  [192,255,0]/255;  ...
        'lightorange',  [255,192,0]/255;  ...
        'grey',  [192,192,192]/255;  ...
        'khaki',  [240,230,140]/255;  ...
        'aquamarine',  [128,255,212]/255;  ...
        'teal',  [0,160,160]/255;  ...
        'steelblue',  [70,130,180]/255;  ...
        'brown',  [165,80,55]/255;  ...
        'olive',  [165,161,55]/255;  ...
        'tomato',  [255,83,76]/255;  ...
        'seashell',  [255,228,196]/255;  ...
        'maroon',  [180,30,30]/255;...
        };
    
    OrganColor=[];
    
    WinColorAll=cell2mat(ColorList(:, 2));
    PinnColorAll=ColorList(:, 1);
    for i=1:size(cellColor, 1)
        WinColor=cellColor{i};
        
        Diff=(WinColorAll(:, 1)-WinColor(1)).^2+(WinColorAll(:, 2)-WinColor(2)).^2+(WinColorAll(:, 3)-WinColor(3)).^2;
        [TempV, TempIndex]=min(Diff);
        
        OrganColor=[OrganColor; PinnColorAll(TempIndex(1))];
    end
    
    
    
    
    
    
    
    
    
    
    
    
    
    
      